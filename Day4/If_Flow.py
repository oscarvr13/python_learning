# elif statement
mascota = "conejo"
if (mascota == "gato"):
    print("Tienes un gato")
elif (mascota == "perro"):
    print("Tienes un perro")
elif (mascota == "pez"):
    print("Tienes un pez")
else:
    print("No tienes ni gato ni perro")

